# Mathematic operators
# plus -> +, minus -> -, multipy -> *, divide -> /
# exponent -> **, quotient -> //, remainder -> %


x = 100
y = 2.5

plus_num = x + y # 102.5
minus_num = x - y # 97.5
multiply_num = x * y # 250.0
divide_num = x / y # 40.0
exponent_num = x ** y # 100,000.0
quotient_num = x // 2.5 # 40.0
remainder_num = x % 2.5 # 0.0

print("x + y = ", plus_num)
print("x - y = ", minus_num)
print ("x * y = ", multiply_num)
print ("x / y = ", divide_num)
print ("x ** y = ", exponent_num)
print ("x // y = ", quotient_num)
print ("x % y = ", remainder_num)
